first_num = int(input("Введите первое число: "))
second_num = int(input("Введите второе число: "))

print(f"{first_num} * {second_num} =", first_num * second_num)
print(f"{first_num} + {second_num} =", first_num + second_num)
print(f"{first_num} - {second_num} =", first_num - second_num)
print(f"{first_num} / {second_num} =", first_num / second_num)